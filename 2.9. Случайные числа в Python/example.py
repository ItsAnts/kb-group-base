from random import randint, random, randrange

random_random = random()
print("СЛУЧАЙНОЕ ЧИСЛО ИЗ ФУНКЦИИ RANDOM: {}".format(random_random))

random_randrange = randrange(10, 56)
print("СЛУЧАЙНОЕ ЧИСЛО ИЗ ФУНКЦИИ RANDRANGE: {}".format(random_randrange))

random_rendint = randint(10, 20)
print("СЛУЧАЙНОЕ ЧИСЛО ИЗ ФУНКЦИИ RANDINT: {}".format(random_rendint))
